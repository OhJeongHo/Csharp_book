using System;

namespace Collection
{
    class Program
    {
        /*
         * 배열처럼 특정 항목의 집합을 리스트 또는 컬렉션이라고 함.
         * 컬렉션은 배열, 리스트, 사전을 사용하여 관련 개체의 그룹을 만들고 관리
         * 
         * 컬렉션 클래스는 데이터 항목의 집합을 메모리상에서 다루는 클래스이다.
         * 
         * C#의 컬렉션은 다음 세 종류.
         * 배열 : 일반적으로 숫자처럼 간단한 데이터 형식을 저장
         * 리스트 : 간단한 데이터 형식을 포함한 개체들을 저장
         * 사전 : 키와 값의 쌍으로 관리되는 개체들을 저장
         * 
         * 일반적으로 기본형 그룹을 배열로 보고, 새로운 타입의 그룹을 컬렉션으로 비교.
         * 
         * 배열 : 정수형, 문자형 등 집합을 나타냄.
         * 컬렉션 : 개체의 집합을 나타냄. 리스트 ,집합, 맵, 사전도 컬렉션과 같은 개념으로 사용.
         * 
         * 컬렉션 관련 클래스들은 먼저 클래스의 인스턴스를 선언해야함.
         * 
         * using 네임스페이스로 사용가능한 항목들.
         * System                       => Array 클래스
         * System.Collection            => Stack / Queue / ArrayList 클래스
         * System.Collection.Generic    => List<T> / LinkedList<T> / Stack<T> / Queue<T> 클래스
         * System.Collection.Concurrent => ConcurrentStack<T> / ConcurrentQueue<T> 클래스
         */
    }
}
